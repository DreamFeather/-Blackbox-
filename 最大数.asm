INCLUDE	IRVINE32.INC
.DATA
DAT	DW	50 DUP(?)
MAX	DW	?
COUNT	DW	?
TIP1	BYTE	'输入几个数？',0
TIP2	BYTE	'请输入数：',0
TIP3	BYTE	'最大的数是：',0
.CODE
START:	XOR	EAX,EAX
	XOR	ECX,ECX
	LEA	EDX,TIP1
	CALL	WRITESTRING
	CALL	READINT
	MOV	COUNT,AX

	LEA	EDX,TIP2
	CALL	WRITESTRING
	MOV	ESI,0
	MOV	CX,COUNT
INPUT:	CALL	READINT
	MOV	DAT[ESI],AX
	ADD	ESI,2
	LOOP	INPUT

	MOV	AX,DAT[0]
	MOV	MAX,AX

	MOV	ESI,2
	MOV	CX,COUNT
	DEC	CX
DEAL:	MOV	AX,DAT[ESI]
	CMP	AX,MAX
	JNA	NEXT
	MOV	MAX,AX
NEXT:	ADD	ESI,2
	LOOP	DEAL

	LEA	EDX,TIP3
	CALL	WRITESTRING
	MOV	AX,MAX
	CALL	WRITEINT
	CALL	CRLF
	EXIT
END	START